        public async Task<Response> CheckConnection()
        {
            //Para validar que el wifi este activo 
            if (!CrossConnectivity.Current.IsConnected)
            {
                return new Response
                {
                    IsSuccess = false,
                    Message = "Habilita tu conexión a internet"
                };
            }

            //Pink a la pagina para verificar la salida a internet
            var isResachable = await CrossConnectivity.Current.IsRemoteReachable("google.com");

            if (!isResachable)
            {
                return new Response
                {
                    IsSuccess = false,
                    Message = "Tu dispositivo no tiene conexión a intenet"
                };
            }

            return new Response
            {
                IsSuccess = true,
                Message = "Ok"
            };

        }

